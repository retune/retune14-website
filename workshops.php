	    	<div class="row" id="networkshop">
				<div class="workshop_info col-md-6 col-xs-12">
					<div class="description col-sm-8 col-sm-offset-2 col-xs-12 col-xs-offset-0" style="padding-bottom:100px;">
						<h2>NETworkshop</h2>
						<h3 style="margin-top: -16px; font-size: 16px; margin-bottom: 22px; line-height: 1.5em;">Lightning in the Age of Cloud-Computing<br/>5 days | 22. – 26.09.2014</h3>
						<p>Ask anyone how the postal system works and they would give a vaguely correct description. Few however would come close to describing how email, let alone a computer network itself, actually functions. With this lack of knowledge comes a risk; we lack the practical understanding to effectively read the infrastructural and political implications of our increased dependency on this technology.<br/>
						In this intense 5 day workshop Oliver and Vasiliev teach low level networking using only command line tools and networking hardware. In doing so, students learn not only how to create and manipulate computer networks, but also how they can be used to manipulate us.<br/>
						No prior knowledge of computer networking, programming or command line interaction is required.</p>
						<p>A small scale model of the Internet is created in class for the purposes of study with which we interact over another self-built local network. By learning about routing, addressing, core protocols, network analysis, network packet capture and dissection, students become dexterous and empowered users of computer networks. At this point students are able to read and traverse wide (the Internet) and local area networks with agility, using methods and tools traditionally the domain of experienced network administrators, hackers and security experts.</p>
						<p>In the second phase of the workshop students learn to read network topologies as political control structures, seeing how corporations and governments shape and control the way we use computer networks.</p>
						<p>Students learn to study these power structures by tracing the flow of packets as they pass over land and sea. <br/>
						Macro-economic and geostrategic speculations are made.</p>
						<p>Finally, encryption and anonymity strategies and theory are addressed, with a mind to defending and asserting the same basic civil rights we uphold in public space.</p>
